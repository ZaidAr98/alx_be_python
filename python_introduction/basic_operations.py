first_number = 10
second_number = 5

sum_of_number = first_number + second_number
print(f"Addition of {first_number} and {second_number} is {sum_of_number}")

difference_of_number = first_number - second_number
print(f"Subtraction of {first_number} and {second_number} is {difference_of_number}")


product_of_number = first_number * second_number
print(f"Multiplication of {first_number} and {second_number} is {product_of_number}")